package algorithm.data_structure.linked_list;

/**
 * 单链表节点
 * Leetcode 707 设计链表
 * */
public class SinglyLinkedListNode {
    /**
     * 节点值
     * */
    int val;
    /**
     * 下一个节点
     * */
    SinglyLinkedListNode next;

    /**
     * 指定节点值的构造方法
     * */
    public SinglyLinkedListNode(int val) {
        this.val = val;
    }
}
